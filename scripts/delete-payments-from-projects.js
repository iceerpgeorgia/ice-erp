const UUID_REGEX = /^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i;

function makeKey(projectUuid, counteragentUuid, financialCodeUuid, currencyUuid) {
  return `${projectUuid}|${counteragentUuid}|${financialCodeUuid}|${currencyUuid}`;
}

async function main() {
  const sinceHours = Number(process.env.SINCE_HOURS || '6');
  const since = new Date(Date.now() - sinceHours * 60 * 60 * 1000);

  const currencyRows = await prisma.currencies.findMany({
    select: { uuid: true, code: true },
  });
  const currencyMap = new Map(currencyRows.map((row) => [row.code.toUpperCase(), row.uuid]));

  const projectIds = [...new Set(INPUT.map(([projectUuid]) => projectUuid))].filter((id) => UUID_REGEX.test(id));
  const projects = await prisma.projects.findMany({
    where: { project_uuid: { in: projectIds } },
    select: { project_uuid: true, counteragent_uuid: true },
  });
  const projectMap = new Map(projects.map((row) => [row.project_uuid, row.counteragent_uuid]));

  const validCombos = new Set();
  const financialCodeIds = new Set();
  const currencyIds = new Set();
  const counteragentIds = new Set();

  for (const [projectUuid, financialCodeUuid, iso] of INPUT) {
    if (!UUID_REGEX.test(projectUuid)) continue;
    if (!UUID_REGEX.test(financialCodeUuid)) continue;
    const counteragentUuid = projectMap.get(projectUuid);
    if (!counteragentUuid) continue;
    const currencyUuid = currencyMap.get(String(iso || '').toUpperCase());
    if (!currencyUuid) continue;

    financialCodeIds.add(financialCodeUuid);
    currencyIds.add(currencyUuid);
    counteragentIds.add(counteragentUuid);
    validCombos.add(makeKey(projectUuid, counteragentUuid, financialCodeUuid, currencyUuid));
  }

  const candidates = await prisma.payments.findMany({
    where: {
      created_at: { gte: since },
      project_uuid: { in: projectIds },
      financial_code_uuid: { in: Array.from(financialCodeIds) },
      counteragent_uuid: { in: Array.from(counteragentIds) },
      currency_uuid: { in: Array.from(currencyIds) },
      job_uuid: null,
      income_tax: false,
    },
    select: { id: true, project_uuid: true, counteragent_uuid: true, financial_code_uuid: true, currency_uuid: true },
  });

  const toDelete = candidates.filter((row) =>
    validCombos.has(makeKey(row.project_uuid, row.counteragent_uuid, row.financial_code_uuid, row.currency_uuid))
  );

  if (!toDelete.length) {
    console.log('No matching payments found to delete.');
    return;
  }

  const deleteResult = await prisma.payments.deleteMany({
    where: { id: { in: toDelete.map((row) => row.id) } },
  });

  console.log(`Deleted: ${deleteResult.count}`);
}

main()
  .catch((err) => {
    console.error('Failed:', err);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
